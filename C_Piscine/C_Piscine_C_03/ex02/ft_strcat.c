char	*ft_strcat(char *dest, char *src)
{
	char *aux;

	aux = dest;
	while (*aux)
	{
		aux++;
	}
	while (*src)
	{
		*aux = *src;
		aux++;
		src++;
	}
	*aux = '\0';
	return (dest);
}
